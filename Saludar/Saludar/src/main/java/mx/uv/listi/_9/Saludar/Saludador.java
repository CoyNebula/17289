package mx.uv.listi._9.saludar;

public class Saludador {
    private String nombre;

    // Constructor
    public Saludador(String nombre) {
        this.nombre = nombre;
    }

    // Getter para 'nombre'
    public String getNombre() {
        return nombre;
    }

    // Setter para 'nombre'
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}